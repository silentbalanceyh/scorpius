public class DoubleETester {
	public static void main(String args[]){
		double x = 3.56E10;
		double y = 3.45E-8;
		System.out.println(x);
		System.out.println(y);
		
		float x1 = 3.56E10f;
		float y1 = 3.45E-8f;
		System.out.println(x1);
		System.out.println(y1);
	}
}
