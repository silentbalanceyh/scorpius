public class ForTester {
	public static void main(String args[]){
		// 初始化条件、循环条件、迭代语句都在一行
		for(int count = 0; count < 10; count++ ){
			System.out.println("\t" + count);
		}
		System.out.println("\t循环结束！");
	}
}
